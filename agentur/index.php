              <h3 class="word--up title--margin">
                But why the hell do you do it?                                              </h3>
              <div class="lead fade--up-p"><p>Hast du dir auch schon gedacht: «Wow, ganz schön viel Meinung für so wenig Ahnung.»?</p>
<p>Ein Gedanke, den Fabian und Jan behaupten nur zu gut, aus ihren vorherigen Webagentur-Jobs, zu kennen. Konsequenterweise haben sie 2010 ihre Jobs an den Nagel gehängt und die localmedia GmbH gegründet. Zeit den grossen Worten auch Taten folgen zu lassen.</p>
<p>Der Plan zu Beginn: einfache Websites für das kleinere, lokale Gewerbe. Preiswert und nahe beim Kunden. Localmedia eben.</p>
<p>«Überraschenderweise» hat sich herausgestellt, dass sich die hohen eigenen Ansprüche nur bedingt mit dieser Strategie befriedigen lassen.</p>
<p>Entsprechend hat sich seit damals so ziemlich alles verändert. Heute zählen nebst ambitionierten KMU, auch bekannte Brands auf unsere smarten Websites. Geblieben ist die Nähe und der direkte Draht zu unseren Kunden. Man munkelt, dass es manche sogar freut, wenn wir anrufen.</p>
<p>Wir tun, was wir tun, weil wir unglaublich viel Fun daran haben, stets die individuell bestmöglichen Lösungen für unsere Kunden zu finden und umzusetzen.</p>
<p>Der, tief in der localmedia DNA verwurzelte, Optimierungsgedanke sorgt dafür, dass wir nicht stillstehen, sondern am digitalen Puls der Zeit leben, Grenzen ausloten und durchbrechen. And this is how work becomes fun 😉</p>
